DROP TABLE IF EXISTS TABLE_NAME_1;
CREATE TABLE IF NOT EXISTS TABLE_NAME_1 (
    TABLE_ATTRIBUTE_1 VARCHAR(1) PRIMARY KEY,
    TABLE_ATTRIBUTE_2 VARCHAR(1) NOT NULL UNIQUE,
    TABLE_ATTRIBUTE_3 VARCHAR(1) NOT NULL
        CHECK (TABLE_ATTRIBUTE_3 NOT LIKE '%[^0-9]%')
);

DROP TABLE IF EXISTS TABLE_NAME_2;
CREATE TABLE IF NOT EXISTS TABLE_NAME_2 (
    TABLE_ATTRIBUTE_1 VARCHAR(1)
        REFERENCES TABLE_NAME_1 (TABLE_ATTRIBUTE_1)
            ON UPDATE CASCADE ON DELETE CASCADE,
    TABLE_ATTRIBUTE_2 VARCHAR(1),
    TABLE_ATTRIBUTE_3 VARCHAR(1) NOT NULL,
    PRIMARY KEY (TABLE_ATTRIBUTE_1, TABLE_ATTRIBUTE_2)
);